// 生成星盘解读
export function generateChartInterpretation(
  planets: { name: string; sign: string; house: number }[],
  aspects: { planet1: string; planet2: string; type: string }[]
): string {
  let interpretation = '# 星盘解读报告\n\n';
  
  // 行星落座解读
  interpretation += '## 行星落座\n\n';
  for (const planet of planets) {
    const signInterp = PLANET_SIGN_INTERPRETATIONS[planet.name]?.[planet.sign];
    const houseInterp = PLANET_HOUSE_INTERPRETATIONS[planet.name]?.[planet.house];
    
    interpretation += `### ${planet.name}在${planet.sign}（第${planet.house}宫）\n`;
    if (signInterp) interpretation += `**落座解读**：${signInterp}\n`;
    if (houseInterp) interpretation += `**宫位解读**：${houseInterp}\n`;
    interpretation += '\n';
  }
  
  // 相位解读
  if (aspects.length > 0) {
    interpretation += '## 重要相位\n\n';
    for (const aspect of aspects) {
      const aspectInfo = ASPECT_INTERPRETATIONS[aspect.type];
      if (aspectInfo) {
        interpretation += `- **${aspect.planet1} ${aspectInfo.symbol} ${aspect.planet2}**（${aspect.type}）：${aspectInfo.description}\n`;
      }
    }
    interpretation += '\n';
  }
  
  // 总结
  interpretation += '## 综合分析\n\n';
  interpretation += '以上是你的星盘主要配置解读。行星落座代表你的基本特质，宫位代表这些特质在生活中的表现领域，相位代表不同能量之间的互动关系。\n\n';
  interpretation += '建议结合整体星盘来看，而不是孤立地理解某一颗行星或某一个相位。\n';
  
  return interpretation;
}
